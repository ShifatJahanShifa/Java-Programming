public class ObjectFeature {
    public String name;
    public int age;
    public String country;
    public String educationalQualification;

    public ObjectFeature()
    {

    }

    public ObjectFeature(String name,int age,String country,String educationalQualification)
    {
        this.name=name;
        this.age=age;
        this.country=country;
        this.educationalQualification=educationalQualification;
    }
}
